<h1>A few words about Ellàs War</h1>

<p><h2>Ellàs War</h2>
Ellas war is a free online strategy game based on ancient Greece. 
The first version, in french, was released on 2007 April, it was a personal project. 

<p><h2>Our team</h2>

The Ellàs War Team is voluntary. 
The money raised by the favors is used to pay the hosting and the various domain names. 
The different paying services can also finance annexes projects like backup server and site related to Ellas War.
