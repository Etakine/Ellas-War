<p>Le grand et puissant roi était là, sur un podium en marbre face à la foule. Leurs efforts venaient d'être récompensés avec l'accession à l'ultime niveau et ils allaient enfin après tant de souffrances, pouvoir s'adonner à l'adoration des dieux.<br/>
La forêt entourant la cité frémit, un vol de corbeau les survola avec un croassement sinistre. Le terrain se mit à trembler, les volcans se réveillèrent, couvrant le ciel d'une nuit sans lune.</p>

<p>La cité sacrifia dix boeufs et dix moutons en l'honneur des dieux, mais rien n'apaisa le ciel. Le roi prit alors une terrible décision, si sa cité était punie ainsi cela voulait dire qu'il avait offensé les dieux et il devait en payer le prix.</p>

<p>Il prit sa plus belle épée, celle-ci, en or, comportait trois pierres, l'une jaune (pour Zeus), une bleu sombre (pour Poséidon) et une troisième grise (pour Hadès). Il brandit la lame face à la foule et la regarda droit dans les yeux.</p>

<p>Soudain, des vertiges le prirent et il tomba au sol. Ses yeux devinrent blancs et de sa bouche sortit une voix rauque.</p>
<p>« Tout ceci n'est que le commencement »</p>
<p>Il releva les yeux et vit l'épée plantée dans le marbre. L'or était devenu sale et ses joyaux avaient changé de couleur. Ils étaient désormais bleu ciel, marron et noir....</p>


